class CirclePerimeterSquare {

    public double perimeter (int radius) {                         //метод определения периметра
        return 2 * Math.PI * radius;
    }


    public double square (int radius) {                            // метод определения площади
        return Math.PI * Math.sqrt(radius);
    }
}



