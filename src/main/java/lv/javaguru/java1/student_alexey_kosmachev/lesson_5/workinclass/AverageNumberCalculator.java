package lv.javaguru.java1.student_alexey_kosmachev.lesson_5.workinclass;

public class AverageNumberCalculator {
    public int findAverage(int number1, int number2) {
        return (number1 + number2) / 2;
    }

    public double findAverageDouble(int number1, int number2) {
        return (number1 + number2) / (double) 2;
    }
}
