package lv.javaguru.java1.student_ainars_belinskis.lesson_2;

public class TwoDoubleSumApp {

    public static void main(String[] args) {
        double number1 = 777.5F;
        double number2 = 437.667F;
        double sumResult = number1 + number2;
        double reduceResult = number1 - number2;
        double multiplyResult = number1 * number2;
        double divisionResult = number1 / number2;

        System.out.println("Sum result = " + sumResult);
        System.out.println("Reduce result = " + reduceResult);
        System.out.println("Multiply result = " + multiplyResult);
        System.out.println("Division result = " + divisionResult);
    }

}
