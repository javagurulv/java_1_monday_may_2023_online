class CelsiaFarengeitConvertor {
    public double CelsiaToFarengeit(double celsia) {
        return celsia * 1.8 + 32;
    }

    public double FarengeitToCelsia(double farengeit) {
        return (farengeit - 32) / 1.8;
    }


}
