package lv.javaguru.java1.student_timur_geldiev.lesson_2.classwork;

public class TwoDoubleSumApp {
    public static void main(String[] args){

        double number1 = 2;
        double number2 = 3;
        double sumResult = number1 + number2;
        double reduceResult = number1 - number2;
        double multiplyResult = number1 * number2;
        double divisionResult = number1 / number2;
        System.out.println("Sum result is " + sumResult);
        System.out.println("Reduce result is " + reduceResult);
        System.out.println("Multiply result is " + multiplyResult);
        System.out.println("Division result is " + divisionResult);

    }
}
