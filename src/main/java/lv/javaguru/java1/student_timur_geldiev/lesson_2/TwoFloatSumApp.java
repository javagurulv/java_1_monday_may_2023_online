package lv.javaguru.java1.student_timur_geldiev.lesson_2;

public class TwoFloatSumApp {
    public static void main(String[] args) {

        float number1 = 12.34F;
        float number2 = 56.78F;
        float sumResult = number1 + number2;
        float reduceResult = number1 - number2;
        float multiplyResult = number1 * number2;
        float divisionResult = number1 / number2;
        System.out.println("Sum result is " + sumResult);
        System.out.println("Reduce result is " + reduceResult);
        System.out.println("Multiply result is " + multiplyResult);
        System.out.println("Division result is " + divisionResult);
    }
}
